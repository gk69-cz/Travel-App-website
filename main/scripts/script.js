function infopackage(){
  if(document.getElementById("packageinfo").style.display = "none"){
    document.getElementById("packageinfo").style.display = "block";
    document.getElementById("packageface").style.display = "none";
  }
  else{
    document.getElementById("packageinfo").style.display = "none";
    document.getElementById("packageface").style.display = "block";
  }
}
